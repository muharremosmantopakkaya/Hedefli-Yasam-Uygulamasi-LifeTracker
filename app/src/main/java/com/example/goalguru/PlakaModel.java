package com.example.goalguru;
public class PlakaModel {
    private String city;
    private String plate;

    public PlakaModel(String city, String plate) {
        this.city = city;
        this.plate = plate;
    }

    public String getCity() {
        return city;
    }

    public String getPlate() {
        return plate;
    }
}





